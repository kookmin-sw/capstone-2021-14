    const check = new Array();
    for(let i=0; i<Shape_Egg.length; i++){
        var count = 0;
        for(let j=0; j<2; j++){
            var range_minus = Shape_Egg[i][j] * 0.99;
            var range_plus = Shape_Egg[i][j] * 1.01;
            if(range_minus < userFace[i][j] && userFace[i][j] < range_plus){
                count ++;
            }
        }
        if(count==2){
            check[i] = 1;
        }
        else{
            check[i] = 0;
        }
        if(check[i]==1){
            console.log(i, "check result: true")
        }
        else{
            console.log(i, "check result: false")
        }
    }

    var check_sum = 0;
    for(let i=0; i<check.length; i++){
        check_sum = check_sum + check[i];
    }
